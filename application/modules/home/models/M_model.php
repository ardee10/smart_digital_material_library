<?php
defined('BASEPATH') or exit('No direct script access allowed');

class M_model extends CI_Model
{

	protected $table = 'tbl_model_assets';

	public function __construct()
	{
		parent::__construct();
	}

	/*
	|--------------------------------------------------------------------------
	| DELETE MODEL ASSETS
	|--------------------------------------------------------------------------
	*/

	public function deleteModelAssets($id)
	{

		/*
		|--------------------------------------------------------------------------
		| GET DATA
		|--------------------------------------------------------------------------
		*/

		$data = $this->db
			->where('id', $id)
			->get($this->table)
			->row();

		/*
		|--------------------------------------------------------------------------
		| NOT FOUND
		|--------------------------------------------------------------------------
		*/

		if (!$data) {
			return false;
		}

		/*
		|--------------------------------------------------------------------------
		| DELETE FILE
		|--------------------------------------------------------------------------
		*/

		if (!empty($data->blender_asset_file)) {

			/*
			|--------------------------------------------------------------------------
			| FULL PATH
			|--------------------------------------------------------------------------
			*/

			$filePath = FCPATH . $data->blender_asset_file;

			/*
			|--------------------------------------------------------------------------
			| CHECK FILE
			|--------------------------------------------------------------------------
			*/

			if (file_exists($filePath)) {

				unlink($filePath);
			}
		}

		/*
		|--------------------------------------------------------------------------
		| DELETE DATABASE
		|--------------------------------------------------------------------------
		*/

		$this->db
			->where('id', $id)
			->delete($this->table);

		/*
		|--------------------------------------------------------------------------
		| SUCCESS
		|--------------------------------------------------------------------------
		*/

		return $this->db->affected_rows() > 0;
	}

	public function updateModelAssets($id, $data)
	{
		$this->db->where('id', $id);
		return $this->db->update('tbl_model_assets', $data);
	}
}

/* End of file: M_model.php */
